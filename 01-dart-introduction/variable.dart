void main() {
  String name = 'Ferdi Pratama';
  int age = 30;
  bool isNight = false;

  // dynamic is a type underlying all Dart objects.
  dynamic mahasiswa = 'Daftar Nama Mahasiswa';
  mahasiswa = 'AMIR,JOE,SONNY';

  print(name); // ferdi pratama
  print(age); // 30
  print(isNight); // false
  print(mahasiswa); // AMIR,JOE,SONNY
}
